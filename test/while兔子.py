"""古典问题:有一对兔子,从出生长到第三个月开始,每个月都生一对兔子,
小兔子第三月开始,每个月都生一个对兔子,假如兔子都不死,
问:到第24个月时,每个月的兔子总数是多少对?"""
"""kjkjkjj"""
sum_01 = 1#第一个月的兔子数
sum_02 = 1#第二个月的兔子数
print(f"第1个月时,每个月的兔子总数是{sum_01}对")
print(f"第2个月时,每个月的兔子总数是{sum_02}对")
month = 3
while month <= 24:
    sum_03 = sum_01 + sum_02#开始繁殖，第三个月的兔子数量
    print(f"第{month}个月时,每个月的兔子总数是{sum_03}对")
    sum_01 = sum_02#第二个月数量
    sum_02 = sum_03#第三个月数量
    month += 1